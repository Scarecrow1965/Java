// Exercise 2.31
// Using only the programming techniques you learned in this chapter,
// write an application that calculates the squares and cubes of the numbers
// from 0 to 10 and prints the resulting values in table format

public class Exercise2_31 {
	// main method begins execution of Java application
	public static void main(String[] args) {
		// set up table
		System.out.println("number \tsquare \tcube");
		// initializing variable
		int number;
		// make the table
		System.out.printf("%d\t %d\t %d%n", number=0, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=1, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=2, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=3, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=4, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=5, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=6, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=7, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=8, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=9, number*number, number*number*number);
		System.out.printf("%d\t %d\t %d%n", number=10, number*number, number*number*number);

	} // end method main

} // end class Exercise2_31
